package ApnaCollege;

public class L14OperatorsAndBinaryNumbers {
    public static void main(String[] args) {
        /*

        //arithmetic operators

        //binary
        // +, -, *, /, %

        // unary : ++ and --
        int a = 1;
        a++;
        System.out.println(a);

        // pre increment unary
        int b = ++a;            //first the value of integer a will increase and then it will be assigned to the integer b

        // post increment unary
        int c = a++;            //first the value of integer a will be assigned to c and then a will increase by one

        // pre decrement
        int d = --a;

        // post decrement
        int e = a--;

         */


        // relational operators: ==, !=, >, <, <=, >=; these operators return a boolean result


        /*
        // logical operators        these too return boolean result
        //
        // logical and  &&      both should be true
        // logical or   ||      one statement should be true
        // logical not  !       it reverses the true value to false and vice versa
        //                      if the expression evaluates to true it will turn it into a false expression
        if (! (a<b))        if a<b is false then the if statement will be true i.e. the code will get executed

         */


        /*
        //bitwise operators


        //binary and: &         returns 1 (true) when both bits are 1 and 0 if one or both bits are zero
        lets say we have a = 0101 and b = 0110 and we use c = a & b then the output will be c = 0100
        //binary or: |          returns 1 when one or both the bots are 1 and 0 if both bits are zero
        c will be equal to 0111
        //binary XOR: ^         returns false if similar values are there and returns 1 when the values are different
        c will be equal to 0011
        binary complement / NOT operator: ~      returns 0 when 1 and 1 when 0
        //binary left shift: <<     the bits will be shifted left
        b << 1 will result in 1100
        //binary right shift: >>        bits will be shifted right
        b >> 2 will result in 0001

         */


        /*
        //assignment operators

        // = operator
        a = b
        // +=
        a += b is equivalent to a = a + b

        similarly -=, *=, /= and %= can also be used

         */
    }
}
